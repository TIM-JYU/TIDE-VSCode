<script lang="ts">
  import { onMount } from 'svelte';
  import type { LoginData } from '../common/types';
  let showSidebarWelcome = false;
  let isLoggedIn = false;
  let loginData: LoginData;

  /**
  * Listens to messages from the extension
  */
  onMount(() => {
      window.addEventListener('message', (event) => {
          const message = event.data;
          if (message && message.type === 'settingValue') {
              showSidebarWelcome = message.value;
          }
          if (message && message.type ==='loginData') {
              loginData = message.value;
          }
      });

      tsvscode.postMessage({ type: 'requestLoggedInStatus', value: '' })
  });

  /**
   * Posts message for the extension for logging user in
   */
  function handleLogin() {
    tsvscode.postMessage({
      type: 'login',
      value: ''
    })
  }

  /**
   * Posts message fot extension for logging user out
   */
  function handleLogout() {
    tsvscode.postMessage({
      type: 'logout',
      value: ''
    })
  }

  $: isLoggedIn = loginData ? loginData.isLogged : false;
</script>
  
{#if !isLoggedIn}
  {#if showSidebarWelcome}
    <div>Welcome to TIDE-extension! Start by logging in.</div>
  {/if}
  <button on:click={handleLogin}>Login</button>
{:else}
  <nav>
    <ul class="nav-list">
      <li>
        <button on:click={() => { tsvscode.postMessage({
          type: 'showCourses',
          value: ''
        })}}>My Courses</button>
      </li>
      <li>
        <button on:click={() => { tsvscode.postMessage({
          type: 'openSettings',
          value: ''
        })}}>Settings</button>
      </li>
      <li>
        <button on:click={handleLogout}>Logout</button>
      </li>
    </ul>
  </nav>
{/if}

<style>
  div {
    color: turquoise;
  }

  button {
    background: none;
    border: none;
    cursor: pointer;
    text-align: left;
  }

  .nav-list {
    padding: 0;
    margin: 0;
    list-style-type: none;
  }
</style>
